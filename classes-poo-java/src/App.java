public class App {
    public static void main(String[] args) throws Exception {
        //Toda estrutura do codigo na linguagem Java é distribuido em arquivos .java denominados de classe.

        /*
         * Classes existentes em nosso projeto serão composta por:
         * Classe(class): A estrutura e/ou representação que direciona a criacao dos objetos de mesmo tipo.
         * Identificador(identity): Proposito existencial aos objetos que serão criados.
         * Caracteristicas(states): Tambem conhecido como atributos ou propriedades, é toda informação que representa o estado do objeto.
         * Comportamentos(behavior): Tambem conhecido como ações ou metodos, é toda parte comportamental que um objeto dispõe.
         * Instaciar(new): É o ato de criar um objeto a partir de estrutura definida em uma classe.
         */

         /*Seguindo algumas convenções, as classes são classificadas como:
          * Classe de modelo(model): classes que representem estrutura de dominio da aplicação, exemplo: Cliente, Pedido, Nota Fiscal, etc.
          * Classe de serviço(service): classes que contem regras de negocio e validação de nosso sistema.
          * Classe de repositorio(repository): classes que contem uma integração com banco de dados.
          * Classe de controle(controller): Classes que possuem a finalidade de disponibilizar alguma comunicação externa a nossa aplicação, tipo http, web ou webservices.
          * Classe utilitaria(util): classe que contem recursos comuns a toda nossa aplicação.
          */
    }
}
